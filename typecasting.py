#type casting is usedto convert the data type of a value to another data type

# there are two types of type casting 
#1. implicit type casting  - itself convert data type to another data types

#2. explicit type casting - user convert data type to another data type using built in functions

#example
# implicit type casting
x = 10  
y = 3.14
z = x + y  # x is implicitly converted to float
print("example of implicit type casting : ",z)  # output: 13.14

# explicit type casting
x = 10
y = 3.14
z = int(y)  # y is explicitly converted to integer
print("example of explicit type casting : ",z)  # output: 3



